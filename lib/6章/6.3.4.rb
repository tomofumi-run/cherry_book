text = '郵便何号は123-4567です'
text[/\d{3}-\d{4}/] #123-4567 マッチした部分を抽出

text = '郵便番号123-4567です'
text.slice(/\d{3}-\d{4}/) #123-4567 []のエイリアスメソッド

text = '郵便番号123-4567です'
text.slice!(/\d{3}-\d{4}/)
text #slice()! 郵便番号です 破壊的メソッド

text = '123,456-789'
