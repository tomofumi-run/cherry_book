'123 456 789'.scan(/\d+/)
#["123", "456", "789"]

'1997年7月17日 2021年7月6日'.scan(/(\d+)年(\d+)月(\d+)日/)
#[["1997", "7", "17"], ["2021", "7", "6"]] キャプチャされる

'1997年7月17日 2021年7月6日'.scan(/(?:\d+)年(?:\d+)月(?:\d+)日/)
# ["1997年7月17日", "2021年7月6日"] キャプチャしない

'1997年7月17日 2021年7月6日'.scan(/\d+年\d+月\d+日/)
